			<article><h3 id="titulos">8 tendencias tecnológicas</h3>
				<p>En esta última edición identificamos 8 tendencias tecnológicas que marcarán las agendas de IT de las compañías en los próximos meses: </p><br>

					<ol>
						<li>Transformación de la función de tecnología. Los departamentos de IT deben transformarse para dar respuesta a las necesidades actuales y futuras de negocio. Este cambio afecta a la infraestructura, a la arquitectura y a la automatización, así como a la organización, en su modelo de colaboración interno en IT y externo con el resto de funciones de la empresa, al mismo tiempo que al modelo de prestación servicios y a su gestión financiera.</li> <br>

						<li>Nueva fuerza de trabajo. Los robots con capacidades cognitivas se incorporarán a la fuerza de trabajo, lo que implicará redefinir la gestión del talento y las organizaciones de las empresas.</li><br>

						<li>La soberanía del dato. Para poder extraer todo el potencial de la información, las organizaciones tendrán que: invertir en calidad; pagar la “data debt”; clasificar los datos usando tecnologías cognitivas y facilitar su explotación mediante el uso de lenguaje natural. </li><br>

						<li>El nuevo “core”. En los últimos años, las inversiones tecnológicas han estado focalizadas en los sistemas de engagement encargados de la relación con el cliente. Ha llegado el momento de revisar los sistemas que han estado soportando los procesos de negocio durante décadas y que, ahora, suponen una limitación en integración, flexibilidad y costes.</li><br>

						<li>Realidad digital. La siguiente fase de la revolución de la realidad aumentada/realidad virtual implica que las empresas establezcan estrategias de casos de uso innovadores y aborden problemas como la integración, la implementación en la nube, la conectividad o el acceso.</li><br>

						<li>Blockchain de blockchains. Esta tecnología evolucionará hasta convertirse en una tecnología de misión crítica dentro de la empresa. Para ello, es importante determinar el nivel de afectación a los procesos de negocio de la empresa, adherirse a estándares y consorcios para proteger las inversiones e incorporar/desarrollar conocimiento en los departamentos de IT.</li><br>

						<li>APIficación. Tradicionalmente, la integración de aplicaciones ha sido una decisión de la función tecnológica. En la actualidad, la identificación, diseño y gestión de las API se ha convertido en una palanca estratégica. Un primer paso en esta disciplina es el diseño de API basados en dominios de información, los cambios de arquitectura tecnológica ante una transaccionalidad desconocida, la gestión del ciclo de vida y la monitorización del uso y definición de KPI.</li><br>

						<li>Observatorio de tecnologías exponenciales.  Los ciclos tecnológicos cada vez son más cortos, por lo que muchas empresas incorporarán funciones de innovación tecnológica dentro de los departamentos de IT, para establecer observatorios que ayuden a monitorizar la evolución de tecnologías emergentes.</li>
					</ol>

